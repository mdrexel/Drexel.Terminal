namespace Drexel.Terminal.Layout
{
    /// <summary>
    /// Vertical alignments.
    /// </summary>
    public enum HorizontalAlignment
    {
        /// <summary>
        /// The left of the content should be aligned with the left of the constraining window.
        /// </summary>
        Left,

        /// <summary>
        /// The center of the content should be aligned with the center of the constraining window.
        /// </summary>
        Center,

        /// <summary>
        /// The right of the content should be aligned with the right of the constraining window.
        /// </summary>
        Right
    }
}
