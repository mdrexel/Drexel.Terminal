namespace Drexel.Terminal.Layout
{
    /// <summary>
    /// Vertical alignments.
    /// </summary>
    public enum VerticalAlignment
    {
        /// <summary>
        /// The top of the content should be aligned with the top of the constraining window.
        /// </summary>
        Top,

        /// <summary>
        /// The center of the content should be aligned with the center of the constraining window.
        /// </summary>
        Center,

        /// <summary>
        /// The bottom of the content should be aligned with the bottom of the constraining window.
        /// </summary>
        Bottom
    }
}
